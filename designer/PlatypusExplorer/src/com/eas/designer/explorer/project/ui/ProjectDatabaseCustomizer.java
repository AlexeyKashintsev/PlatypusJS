    private void btnTryConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTryConnectActionPerformed
        try {
            if (project.getClient() == null) {
                project.startConnecting2db();
            } else {
                project.disconnectFormDb();
            }
        } catch (InterruptedException | ExecutionException ex) {
            Logger.getLogger(ProjectDatabaseCustomizer.class.getName()).log(Level.WARNING, null, ex);
        }
    }//GEN-LAST:event_btnTryConnectActionPerformed

